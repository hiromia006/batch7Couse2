package javaClassess.class1;

public class DataTypeTest {
    public static void main(String[] args) {
        int number = 80;
        long world_length = 78945612312l;
        double pi = 3.14;
        float amount = 53.25f;
        boolean has_id_card = true;
        char first_letter = 'A';

        System.out.println("int = " + number);
        System.out.println("long = " + world_length);
        System.out.println("double = " + pi);
        System.out.println("float = " + amount);
        System.out.println("boolean = " + has_id_card);
        System.out.println("char = " + first_letter);

    }
}
