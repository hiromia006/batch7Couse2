package javaClassess.class4;

public class ForLoopTest {
    public static void main(String[] args) {
        // Java for loop: for(initialization, condition, increment/decrement)
        for (int i = 0; i <= 10; i += 2) {  // i=i+2;0, 2, 4, 6, 8, 10
            System.out.println("For increment Loop " + i);
        }
        System.out.println("====================================================");
        for (int i = 30; i >= -10; i -= 5) {  //shahida=7, wasid=8, farhida=8, Leemon=9,rishad=9
            System.out.println("For decrement Loop "+i);
        }


    }
}
